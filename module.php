        <h2 class="lesson-title-main">Basic Concepts of UI (User Interface) Design</h2>
        <p>
            User Interface (UI) Design focuses on creating the visual and interactive elements of a digital product, such as websites, mobile apps, and software. The goal is to make the interface easy to use, visually appealing, and consistent.
        </p>

        <h3 class="lesson-subtitle">What Is UI Design?</h3>
        <p>
            UI Design is the process of designing the layout, look, and feel of a digital interface. It involves choosing the right typography, colors, icons, spacing, and visual hierarchy so users can interact with the product smoothly.
        </p>
        
        <h3 class="lesson-subtitle">Key Principles of UI Design</h3>
        <div class="principle-list">
            <p><strong>a. Clarity</strong></p>
            <p>Every element should be easy to understand. Text, icons, and buttons must clearly communicate their purpose.</p>
            
            <p><strong>b. Consistency</strong></p>
            <p>Use consistent colors, typography, spacing, and components throughout the product to create familiarity and reduce confusion.</p>
            
            <p><strong>c. Visual Hierarchy</strong></p>
            <p>Arrange elements based on importance. Larger, bolder, or brighter elements attract attention first and guide user actions.</p>
